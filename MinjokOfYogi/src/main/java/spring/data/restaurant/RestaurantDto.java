package spring.data.restaurant;

public class RestaurantDto {

	private int RST_id;
	private int OWN_id;
	private String RST_name;
	private String RST_tel;
	private int RST_category;
	private String RST_location;
	private String RST_license;
	private int RST_auth;
	
	public int getRST_id() {
		return RST_id;
	}
	public void setRST_id(int rST_id) {
		RST_id = rST_id;
	}
	public int getOWN_id() {
		return OWN_id;
	}
	public void setOWN_id(int oWN_id) {
		OWN_id = oWN_id;
	}
	public String getRST_name() {
		return RST_name;
	}
	public void setRST_name(String rST_name) {
		RST_name = rST_name;
	}
	public String getRST_tel() {
		return RST_tel;
	}
	public void setRST_tel(String rST_tel) {
		RST_tel = rST_tel;
	}
	public int getRST_category() {
		return RST_category;
	}
	public void setRST_category(int rST_category) {
		RST_category = rST_category;
	}
	public String getRST_location() {
		return RST_location;
	}
	public void setRST_location(String rST_location) {
		RST_location = rST_location;
	}
	public int getRST_auth() {
		return RST_auth;
	}
	public void setRST_auth(int rST_auth) {
		RST_auth = rST_auth;
	}
	public String getRST_license() {
		return RST_license;
	}
	public void setRST_license(String rST_license) {
		RST_license = rST_license;
	}
	
	
}
